#include <iostream> 
using namespace std; 

/*整數字面常量可以用 8 進位、10 進位與 16 進位，一般習慣使用 10 進位，
如果要使用 8 進位的字面常量，開頭加上 0 就可以了，如果要使用 16 進位字面常量，
開頭加上 0x，C++ 14 支援 2 進位表示，使用 0b 開頭。*/

int main() { 
    cout << 26 << "\n";       // 10 進位
    cout << 032 << "\n";      // 8 進位
    cout << 0x1A << "\n";     // 16 進位
    cout << 0b11010 << "\n";  // 2 進位（C++ 14）

    return 0; 
}